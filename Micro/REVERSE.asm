.MODEL SMALL
.STACK 100H
.DATA
STRING1 DB 'RAJSHAHI'
STRING2 DB 8 DUP(?)
.CODE
MAIN PROC
    MOV AX,00H
    MOV AX,@DATA
    MOV DS,AX
    MOV ES,AX
    LEA SI,STRING1
    LEA DI,STRING2
    STD  
    
    TOP:
    CMP DI,00H
    JE EXIT
    MOVSB
    MOV DL,[DI]
    MOV AH,2
    INT 21H
    JMP TOP
    EXIT:
    MAIN ENDP
END MAIN